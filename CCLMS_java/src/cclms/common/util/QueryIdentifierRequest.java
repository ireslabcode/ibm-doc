package cclms.common.util;

public class QueryIdentifierRequest {
	private String selectedLobby;
	private String selectedTractionType;
	private String selectedTrainType;
	private String selectedTrainFrequency;
	
	
	
	
	
	
	
	
	public String getSelectedLobby() {
		return selectedLobby;
	}
	public void setSelectedLobby(String selectedLobby) {
		this.selectedLobby = selectedLobby;
	}
	public String getSelectedTractionType() {
		return selectedTractionType;
	}
	public void setSelectedTractionType(String selectedTractionType) {
		this.selectedTractionType = selectedTractionType;
	}
	public String getSelectedTrainType() {
		return selectedTrainType;
	}
	public void setSelectedTrainType(String selectedTrainType) {
		this.selectedTrainType = selectedTrainType;
	}
	public String getSelectedTrainFrequency() {
		return selectedTrainFrequency;
	}
	public void setSelectedTrainFrequency(String selectedTrainFrequency) {
		this.selectedTrainFrequency = selectedTrainFrequency;
	}
	
	
	
	
	

}
